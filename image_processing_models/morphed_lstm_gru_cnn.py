def create_enhanced_cnn_model(input_shape=(224, 224, 3)):
    """Enhanced CNN model for image forensics"""
    model = keras.Sequential([
        layers.Conv2D(32, (3, 3), activation='relu', padding='same', input_shape=input_shape),
        layers.BatchNormalization(),
        layers.Conv2D(32, (3, 3), activation='relu', padding='same'),
        layers.MaxPooling2D(2, 2),
        layers.Dropout(0.2),

        layers.Conv2D(64, (3, 3), activation='relu', padding='same'),
        layers.BatchNormalization(),
        layers.Conv2D(64, (3, 3), activation='relu', padding='same'),
        layers.MaxPooling2D(2, 2),
        layers.Dropout(0.3),

        layers.Conv2D(128, (3, 3), activation='relu', padding='same'),
        layers.BatchNormalization(),
        layers.Conv2D(128, (3, 3), activation='relu', padding='same'),
        layers.MaxPooling2D(2, 2),
        layers.Dropout(0.4),

        layers.Conv2D(256, (3, 3), activation='relu', padding='same'),
        layers.BatchNormalization(),
        layers.GlobalAveragePooling2D(),

        layers.Dense(512, activation='relu', kernel_regularizer=keras.regularizers.l2(0.001)),
        layers.BatchNormalization(),
        layers.Dropout(0.5),
        layers.Dense(256, activation='relu', kernel_regularizer=keras.regularizers.l2(0.001)),
        layers.Dropout(0.3),
        layers.Dense(1, activation='sigmoid')
    ])

    return model

def create_lstm_model(input_shape=(4, 56, 56, 3)):
    """LSTM model for sequential analysis"""
    model = keras.Sequential([
        layers.TimeDistributed(layers.Conv2D(32, (3, 3), activation='relu', padding='same'), input_shape=input_shape),
        layers.TimeDistributed(layers.BatchNormalization()),
        layers.TimeDistributed(layers.MaxPooling2D(2, 2)),
        layers.TimeDistributed(layers.Dropout(0.2)),

        layers.TimeDistributed(layers.Conv2D(64, (3, 3), activation='relu', padding='same')),
        layers.TimeDistributed(layers.BatchNormalization()),
        layers.TimeDistributed(layers.MaxPooling2D(2, 2)),
        layers.TimeDistributed(layers.Flatten()),

        layers.LSTM(128, return_sequences=True, dropout=0.3, recurrent_dropout=0.3),
        layers.LSTM(64, dropout=0.3, recurrent_dropout=0.3),

        layers.Dense(64, activation='relu', kernel_regularizer=keras.regularizers.l2(0.001)),
        layers.Dropout(0.4),
        layers.Dense(32, activation='relu', kernel_regularizer=keras.regularizers.l2(0.001)),
        layers.Dropout(0.3),
        layers.Dense(1, activation='sigmoid')
    ])
    return model

def create_gru_model(input_shape=(4, 56, 56, 3)):
    """GRU model for efficient sequential analysis"""
    model = keras.Sequential([
        layers.TimeDistributed(layers.Conv2D(32, (3, 3), activation='relu', padding='same'), input_shape=input_shape),
        layers.TimeDistributed(layers.BatchNormalization()),
        layers.TimeDistributed(layers.MaxPooling2D(2, 2)),
        layers.TimeDistributed(layers.Dropout(0.2)),

        layers.TimeDistributed(layers.Conv2D(64, (3, 3), activation='relu', padding='same')),
        layers.TimeDistributed(layers.BatchNormalization()),
        layers.TimeDistributed(layers.MaxPooling2D(2, 2)),
        layers.TimeDistributed(layers.Flatten()),

        layers.GRU(128, return_sequences=True, dropout=0.3, recurrent_dropout=0.3),
        layers.GRU(64, dropout=0.3, recurrent_dropout=0.3),

        layers.Dense(64, activation='relu', kernel_regularizer=keras.regularizers.l2(0.001)),
        layers.Dropout(0.4),
        layers.Dense(32, activation='relu', kernel_regularizer=keras.regularizers.l2(0.001)),
        layers.Dropout(0.3),
        layers.Dense(1, activation='sigmoid')
    ])
    return model

def prepare_sequential_data(X_data, seq_length=4):
    """Convert image data to sequential format"""
    if len(X_data) == 0:
        return np.array([])

    X_seq = []
    for img in X_data:
        patches = []
        h, w = img.shape[0], img.shape[1]
        patch_size = h // seq_length

        for i in range(seq_length):
            start_i = i * patch_size
            end_i = min((i + 1) * patch_size, h)
            # Take horizontal slice
            patch = img[start_i:end_i, :56]
            # Resize to fixed size
            patch_resized = cv2.resize(patch, (56, 56))
            patches.append(patch_resized)

        X_seq.append(np.array(patches))

    return np.array(X_seq)

def evaluate_model_comprehensive(model, X_test, y_test, model_name, is_sequential=False):
    """Comprehensive model evaluation with detailed metrics"""
    print(f"\n{'='*50}")
    print(f"EVALUATING {model_name}")
    print(f"{'='*50}")

    if len(X_test) == 0:
        print("❌ No test data available")
        return {'accuracy': 0, 'precision': 0, 'recall': 0, 'f1': 0, 'roc_auc': 0}

    try:
        if is_sequential:
            X_test_processed = prepare_sequential_data(X_test)
        else:
            X_test_processed = X_test

        # Ensure we have valid data
        if len(X_test_processed) == 0:
            print("❌ No valid test data after processing")
            return {'accuracy': 0, 'precision': 0, 'recall': 0, 'f1': 0, 'roc_auc': 0}

        # Predictions
        y_pred_proba = model.predict(X_test_processed, verbose=0)
        y_pred = (y_pred_proba > 0.5).astype(int).flatten()

        # Handle single class case
        if len(np.unique(y_test)) < 2:
            print("⚠️ Only one class present in test data")
            accuracy = accuracy_score(y_test, y_pred)
            print(f"📊 Accuracy: {accuracy:.4f}")
            return {'accuracy': accuracy, 'precision': 0, 'recall': 0, 'f1': 0, 'roc_auc': 0.5}

        # Calculate metrics
        accuracy = accuracy_score(y_test, y_pred)
        precision = precision_score(y_test, y_pred, zero_division=0)
        recall = recall_score(y_test, y_pred, zero_division=0)
        f1 = f1_score(y_test, y_pred, zero_division=0)

        # ROC AUC (handle edge cases)
        try:
            if len(np.unique(y_pred_proba)) > 1:
                roc_auc = roc_auc_score(y_test, y_pred_proba)
            else:
                roc_auc = 0.5
        except:
            roc_auc = 0.5  # Default for edge cases

        print(f"📊 Accuracy: {accuracy:.4f}")
        print(f"📊 Precision: {precision:.4f}")
        print(f"📊 Recall: {recall:.4f}")
        print(f"📊 F1-Score: {f1:.4f}")
        print(f"📊 ROC-AUC: {roc_auc:.4f}")

        # Detailed classification report
        print(f"\n📋 Detailed Classification Report:")
        print(classification_report(y_test, y_pred, target_names=['Authentic', 'Tampered']))

        # Confusion Matrix
        cm = confusion_matrix(y_test, y_pred)

        plt.figure(figsize=(8, 6))
        sns.heatmap(cm, annot=True, fmt='d', cmap='Blues',
                   xticklabels=['Authentic', 'Tampered'],
                   yticklabels=['Authentic', 'Tampered'])
        plt.title(f'{model_name} - Confusion Matrix')
        plt.ylabel('True Label')
        plt.xlabel('Predicted Label')
        plt.show()

        # ROC Curve
        if len(np.unique(y_pred_proba)) > 1:
            fpr, tpr, _ = roc_curve(y_test, y_pred_proba)

            plt.figure(figsize=(8, 6))
            plt.plot(fpr, tpr, color='darkorange', lw=2, label=f'ROC curve (area = {roc_auc:.2f})')
            plt.plot([0, 1], [0, 1], color='navy', lw=2, linestyle='--')
            plt.xlim([0.0, 1.0])
            plt.ylim([0.0, 1.05])
            plt.xlabel('False Positive Rate')
            plt.ylabel('True Positive Rate')
            plt.title(f'{model_name} - ROC Curve')
            plt.legend(loc="lower right")
            plt.grid(True, alpha=0.3)
            plt.show()
        else:
            print("⚠️ Cannot plot ROC curve (single probability value)")

        return {
            'accuracy': accuracy,
            'precision': precision,
            'recall': recall,
            'f1': f1,
            'roc_auc': roc_auc
        }

    except Exception as e:
        print(f"❌ Error evaluating {model_name}: {e}")
        import traceback
        traceback.print_exc()
        return {'accuracy': 0, 'precision': 0, 'recall': 0, 'f1': 0, 'roc_auc': 0}

def predict_with_all_models(image_path, cnn_model, lstm_model, gru_model):
    """Predict using all three models"""
    try:
        # Load and preprocess image
        img = cv2.imread(image_path)
        if img is None:
            print(f"❌ Cannot read image: {image_path}")
            return None, None

        preprocessor = AdvancedImagePreprocessor()
        processed_img = preprocessor.apply_advanced_preprocessing(img)

        if processed_img is None:
            print("❌ Failed to preprocess image")
            return None, None

        # CNN prediction
        img_input = np.expand_dims(processed_img, axis=0)
        cnn_pred = cnn_model.predict(img_input, verbose=0)[0][0]

        # LSTM prediction
        img_seq = prepare_sequential_data(np.array([processed_img]))
        if len(img_seq) > 0:
            lstm_pred = lstm_model.predict(img_seq, verbose=0)[0][0]
        else:
            lstm_pred = 0.5  # Default if sequential conversion fails

        # GRU prediction
        if len(img_seq) > 0:
            gru_pred = gru_model.predict(img_seq, verbose=0)[0][0]
        else:
            gru_pred = 0.5  # Default if sequential conversion fails

        predictions = {
            'Enhanced CNN': cnn_pred,
            'LSTM': lstm_pred,
            'GRU': gru_pred
        }

        return predictions, processed_img

    except Exception as e:
        print(f"❌ Prediction error: {e}")
        return None, None

