# COMPREHENSIVE PREPROCESSING
class AdvancedImagePreprocessor:
    """Advanced image preprocessing for image forensics"""

    def __init__(self, img_size=IMG_SIZE):
        self.img_size = img_size

    def apply_advanced_preprocessing(self, image):
        """Apply comprehensive preprocessing pipeline"""
        if image is None:
            return None

        processed_image = image.copy()

        # STEP 1: Ensure image has 3 channels
        if len(processed_image.shape) == 2:
            processed_image = cv2.cvtColor(processed_image, cv2.COLOR_GRAY2BGR)

        # STEP 2: Color Space Conversion
        processed_image = cv2.cvtColor(processed_image, cv2.COLOR_BGR2RGB)

        # STEP 3: Noise Reduction (adaptive)
        if processed_image.shape[0] > 100 and processed_image.shape[1] > 100:
            processed_image = cv2.GaussianBlur(processed_image, (3, 3), 0)

        # STEP 4: Histogram Equalization (CLAHE) for better contrast
        lab = cv2.cvtColor(processed_image, cv2.COLOR_RGB2LAB)
        lab_planes = list(cv2.split(lab))
        clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
        lab_planes[0] = clahe.apply(lab_planes[0])
        lab = cv2.merge(lab_planes)
        processed_image = cv2.cvtColor(lab, cv2.COLOR_LAB2RGB)

        # STEP 5: Edge preservation (optional, can help with tampering detection)
        processed_image = cv2.bilateralFilter(processed_image, 9, 75, 75)

        # STEP 6: Resize to target size
        processed_image = cv2.resize(processed_image, self.img_size)

        # STEP 7: Normalize pixel values to [0,1]
        processed_image = processed_image.astype('float32') / 255.0

        return processed_image

