# ==================== VIDEO UPLOAD AND PREDICTION SYSTEM ====================
class VideoMorphingDetector:
    def __init__(self, config, model_choice='ensemble'):
        self.config = config
        self.preprocessor = EnhancedVideoPreprocessor(config)
        self.model_choice = model_choice

        # Load models
        self.resnet_model = self._load_model('Enhanced_ResNet')
        self.eff_model = self._load_model('Enhanced_EfficientNet')

        print("✅ Models loaded successfully!")

    def _load_model(self, model_name):
        """Load trained model"""
        model_path = os.path.join(self.config.MODEL_PATH, model_name, 'best_model.pth')

        if model_name == 'Enhanced_ResNet':
            model = EnhancedResNet(config)
        else:
            model = EnhancedEfficientNet(config)

        if os.path.exists(model_path):
            checkpoint = torch.load(model_path, map_location=device)
            model.load_state_dict(checkpoint['model_state_dict'])
            model.to(device)
            model.eval()
            print(f"✅ Loaded {model_name}")
        else:
            print(f"⚠️ No trained {model_name} found, using untrained model")
            model.to(device)
            model.eval()

        return model

    def predict_video(self, video_path):
        """Predict if a video is morphed or not"""
        if not os.path.exists(video_path):
            return {
                'error': 'Video file not found',
                'prediction': 'ERROR',
                'confidence': 0.0
            }

        try:
            # Preprocess video
            frames = self.preprocessor.preprocess_video(video_path, is_training=False)
            frames = frames.unsqueeze(0).to(device)

            predictions = []
            confidences = []

            # Get prediction from ResNet
            with torch.no_grad():
                resnet_output = self.resnet_model(frames)
                resnet_probs = F.softmax(resnet_output, dim=1)
                resnet_pred = torch.argmax(resnet_probs, dim=1).item()
                resnet_conf = resnet_probs[0][resnet_pred].item()
                predictions.append(resnet_pred)
                confidences.append(resnet_conf)

                # Get prediction from EfficientNet
                eff_output = self.eff_model(frames)
                eff_probs = F.softmax(eff_output, dim=1)
                eff_pred = torch.argmax(eff_probs, dim=1).item()
                eff_conf = eff_probs[0][eff_pred].item()
                predictions.append(eff_pred)
                confidences.append(eff_conf)

            # Ensemble prediction
            if self.model_choice == 'ensemble':
                # Weighted average based on confidence
                total_conf = sum(confidences)
                weighted_pred = sum(p * c for p, c in zip(predictions, confidences)) / total_conf
                final_pred = 1 if weighted_pred > 0.5 else 0
                final_conf = weighted_pred if final_pred == 1 else 1 - weighted_pred
            elif self.model_choice == 'resnet':
                final_pred = predictions[0]
                final_conf = confidences[0]
            else:  # efficientnet
                final_pred = predictions[1]
                final_conf = confidences[1]

            # Interpret result
            label = "MORPHED/FAKE" if final_pred == 1 else "REAL/AUTHENTIC"

            return {
                'prediction': label,
                'confidence': float(final_conf * 100),
                'real_probability': float((1 - weighted_pred) * 100) if self.model_choice == 'ensemble' else float((1 - final_conf) * 100),
                'fake_probability': float(weighted_pred * 100) if self.model_choice == 'ensemble' else float(final_conf * 100),
                'resnet_prediction': 'FAKE' if predictions[0] == 1 else 'REAL',
                'resnet_confidence': float(confidences[0] * 100),
                'efficientnet_prediction': 'FAKE' if predictions[1] == 1 else 'REAL',
                'efficientnet_confidence': float(confidences[1] * 100)
            }

        except Exception as e:
            return {
                'error': str(e),
                'prediction': 'ERROR',
                'confidence': 0.0
            }

    def visualize_prediction(self, video_path, result):
        """Visualize prediction results"""
        fig, axes = plt.subplots(2, 3, figsize=(15, 10))

        # Extract first frame for visualization
        cap = cv2.VideoCapture(video_path)
        ret, frame = cap.read()
        cap.release()

        if ret:
            # Original frame
            axes[0, 0].imshow(cv2.cvtColor(frame, cv2.COLOR_BGR2RGB))
            axes[0, 0].set_title('Original Video Frame')
            axes[0, 0].axis('off')

            # Try to detect and show face
            bbox = self.preprocessor.step2_face_detection_ensemble(frame)
            if bbox:
                x, y, w, h = bbox
                frame_with_bbox = frame.copy()
                cv2.rectangle(frame_with_bbox, (x, y), (x+w, y+h), (0, 255, 0), 2)
                axes[0, 1].imshow(cv2.cvtColor(frame_with_bbox, cv2.COLOR_BGR2RGB))
                axes[0, 1].set_title('Face Detection')
            else:
                axes[0, 1].imshow(cv2.cvtColor(frame, cv2.COLOR_BGR2RGB))
                axes[0, 1].set_title('No Face Detected')
            axes[0, 1].axis('off')

        # Prediction probabilities bar chart
