class EnhancedResNet(nn.Module):
    """Enhanced ResNet50 with temporal attention"""
    def __init__(self, config):
        super().__init__()
        self.config = config

        # Load pretrained ResNet50
        try:
            backbone = models.resnet50(pretrained=True)
        except:
            backbone = models.resnet50(weights='DEFAULT')

        # Remove final layers
        self.feature_extractor = nn.Sequential(*list(backbone.children())[:-2])

        # Global average pooling
        self.global_pool = nn.AdaptiveAvgPool2d((1, 1))

        # Temporal attention
        self.temporal_attention = nn.Sequential(
            nn.Linear(2048, 512),
            nn.ReLU(),
            nn.Dropout(config.DROPOUT),
            nn.Linear(512, config.FRAMES_PER_VIDEO),
            nn.Softmax(dim=1)
        )

        # Classification head
        self.classifier = nn.Sequential(
            nn.Linear(2048 * config.FRAMES_PER_VIDEO, 1024),
            nn.ReLU(),
            nn.Dropout(config.DROPOUT),
            nn.Linear(1024, 512),
            nn.ReLU(),
            nn.Dropout(config.DROPOUT),
            nn.Linear(512, config.NUM_CLASSES)
        )

    def forward(self, x):
        # x: [B, T, C, H, W]
        batch_size, timesteps, C, H, W = x.size()

        # Process each frame
        frame_features = []
        for t in range(timesteps):
            frame = x[:, t, :, :, :]
            features = self.feature_extractor(frame)
            features = self.global_pool(features)
            features = features.view(batch_size, -1)
            frame_features.append(features)

        # Stack features
        features = torch.stack(frame_features, dim=1)  # [B, T, 2048]

        # Apply temporal attention
        attention_weights = self.temporal_attention(features.mean(dim=2))  # [B, T]
        attention_weights = attention_weights.unsqueeze(2)  # [B, T, 1]
        weighted_features = features * attention_weights

        # Flatten for classification
        flattened = weighted_features.view(batch_size, -1)

        # Classification
        output = self.classifier(flattened)

        return output

class EnhancedEfficientNet(nn.Module):
    """Enhanced EfficientNet-B4 with spatial attention"""
    def __init__(self, config):
        super().__init__()
        self.config = config

        # Load pretrained EfficientNet-B4
        try:
            backbone = models.efficientnet_b4(pretrained=True)
        except:
            backbone = models.efficientnet_b4(weights='DEFAULT')

        # Remove final layers
        self.feature_extractor = nn.Sequential(*list(backbone.children())[:-2])

        # Get feature dimension
        self.feature_dim = 1792  # EfficientNet-B4 feature dimension

        # Spatial attention
        self.spatial_attention = nn.Sequential(
            nn.Conv2d(self.feature_dim, self.feature_dim // 16, kernel_size=1),
            nn.ReLU(),
            nn.Conv2d(self.feature_dim // 16, 1, kernel_size=1),
            nn.Sigmoid()
        )

        # Global pooling
        self.global_pool = nn.AdaptiveAvgPool2d((1, 1))

        # Classification head
        self.classifier = nn.Sequential(
            nn.Linear(self.feature_dim * config.FRAMES_PER_VIDEO, 1024),
            nn.ReLU(),
            nn.Dropout(config.DROPOUT),
            nn.Linear(1024, 512),
            nn.ReLU(),
            nn.Dropout(config.DROPOUT),
            nn.Linear(512, config.NUM_CLASSES)
        )

    def forward(self, x):
        # x: [B, T, C, H, W]
        batch_size, timesteps, C, H, W = x.size()

        # Process each frame
        frame_features = []
        for t in range(timesteps):
            frame = x[:, t, :, :, :]
            features = self.feature_extractor(frame)

            # Apply spatial attention
            attention_map = self.spatial_attention(features)
            attended_features = features * attention_map

            # Global pooling
            pooled = self.global_pool(attended_features)
            pooled = pooled.view(batch_size, -1)
            frame_features.append(pooled)

        # Concatenate all frame features
        features = torch.cat(frame_features, dim=1)

        # Classification
        output = self.classifier(features)

        return output

# ==================== TRAINER ====================
class ModelTrainer:
    def __init__(self, model, config, model_name):
        self.model = model.to(device)
        self.config = config
        self.model_name = model_name

        # Optimizer with weight decay
        self.optimizer = optim.AdamW(
            model.parameters(),
            lr=config.LEARNING_RATE,
            weight_decay=1e-4,
            betas=(0.9, 0.999)
        )

        # Loss function with class weights
        self.criterion = nn.CrossEntropyLoss()

        # Learning rate scheduler
        self.scheduler = optim.lr_scheduler.CosineAnnealingLR(
            self.optimizer,
            T_max=config.EPOCHS,
            eta_min=1e-6
        )

        # Early stopping
        self.best_accuracy = 0
        self.patience_counter = 0

        # Create save directory
        self.save_path = os.path.join(config.MODEL_PATH, model_name)
        os.makedirs(self.save_path, exist_ok=True)

    def train_epoch(self, train_loader, epoch):
        self.model.train()
        total_loss = 0
        correct = 0
        total = 0

        pbar = tqdm(train_loader, desc=f'Epoch {epoch+1}/{self.config.EPOCHS}')
        for batch_idx, (data, target, _) in enumerate(pbar):
            data, target = data.to(device), target.to(device)

            self.optimizer.zero_grad()
            output = self.model(data)
            loss = self.criterion(output, target)
            loss.backward()

            # Gradient clipping
            torch.nn.utils.clip_grad_norm_(self.model.parameters(), max_norm=1.0)

            self.optimizer.step()

            total_loss += loss.item()
            _, predicted = output.max(1)
            total += target.size(0)
            correct += predicted.eq(target).sum().item()

            pbar.set_postfix({
                'Loss': f'{loss.item():.4f}',
                'Acc': f'{100.*correct/total:.2f}%'
            })

        return total_loss / len(train_loader), 100. * correct / total

    def validate(self, val_loader):
        self.model.eval()
        total_loss = 0
        correct = 0
        total = 0
        all_preds = []
        all_targets = []
        all_probs = []

        with torch.no_grad():
            for data, target, _ in val_loader:
                data, target = data.to(device), target.to(device)
                output = self.model(data)
                loss = self.criterion(output, target)
                probs = F.softmax(output, dim=1)

                total_loss += loss.item()
                _, predicted = output.max(1)
                total += target.size(0)
                correct += predicted.eq(target).sum().item()

                all_preds.extend(predicted.cpu().numpy())
                all_targets.extend(target.cpu().numpy())
                all_probs.extend(probs.cpu().numpy())

        accuracy = 100. * correct / total
        return total_loss / len(val_loader), accuracy, all_preds, all_targets, all_probs

    def train(self, train_loader, val_loader):
        history = {
            'train_loss': [], 'train_acc': [],
            'val_loss': [], 'val_acc': [],
            'learning_rates': []
        }

        for epoch in range(self.config.EPOCHS):
            print(f'\n{"="*60}')
            print(f'Epoch {epoch+1}/{self.config.EPOCHS}')
            print(f'{"="*60}')

            train_loss, train_acc = self.train_epoch(train_loader, epoch)
            val_loss, val_acc, _, _, _ = self.validate(val_loader)

            # Update learning rate
            self.scheduler.step()
            current_lr = self.optimizer.param_groups[0]['lr']

            # Save history
            history['train_loss'].append(train_loss)
            history['train_acc'].append(train_acc)
            history['val_loss'].append(val_loss)
            history['val_acc'].append(val_acc)
            history['learning_rates'].append(current_lr)

            print(f'Train Loss: {train_loss:.4f}, Train Acc: {train_acc:.2f}%')
            print(f'Val Loss: {val_loss:.4f}, Val Acc: {val_acc:.2f}%')
            print(f'Learning Rate: {current_lr:.6f}')

            # Save best model
            if val_acc > self.best_accuracy:
                self.best_accuracy = val_acc
                self.patience_counter = 0

                torch.save({
                    'epoch': epoch,
                    'model_state_dict': self.model.state_dict(),
                    'optimizer_state_dict': self.optimizer.state_dict(),
                    'scheduler_state_dict': self.scheduler.state_dict(),
                    'accuracy': val_acc,
                    'loss': val_loss,
                    'config': self.config
                }, os.path.join(self.save_path, 'best_model.pth'))

                print(f'🚀 Best model saved with accuracy: {val_acc:.2f}%')
            else:
                self.patience_counter += 1

            # Early stopping
            if self.patience_counter >= self.config.PATIENCE:
                print(f'⏹️ Early stopping at epoch {epoch+1}')
                break

        # Load best model
        best_model_path = os.path.join(self.save_path, 'best_model.pth')
        if os.path.exists(best_model_path):
            checkpoint = torch.load(best_model_path, map_location=device)
            self.model.load_state_dict(checkpoint['model_state_dict'])

        return history

    def evaluate(self, test_loader):
        self.model.eval()
        all_preds = []
        all_targets = []
        all_probs = []

        with torch.no_grad():
            for data, target, _ in test_loader:
                data, target = data.to(device), target.to(device)
                output = self.model(data)
                probs = F.softmax(output, dim=1)

                _, predicted = output.max(1)

                all_preds.extend(predicted.cpu().numpy())
                all_targets.extend(target.cpu().numpy())
                all_probs.extend(probs.cpu().numpy())

        return all_preds, all_targets, all_probs

